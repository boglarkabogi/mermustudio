function setLanguage(lang) {
  document.querySelectorAll("[data-i18n]").forEach(el => {
    const key = el.getAttribute("data-i18n");
    const placeholderKey = el.getAttribute("data-i18n-placeholder");

    if (translations[lang] && translations[lang][key]) {
      if (placeholderKey && translations[lang][placeholderKey]) {
        el.placeholder = translations[lang][placeholderKey];
      }
      el.innerHTML = translations[lang][key];
    }
  });

  const btn = document.getElementById("lang-toggle");
  if (btn) btn.textContent = lang === "hu" ? "EN" : "HU";

  localStorage.setItem("lang", lang);
}

function initLanguage() {
  const saved = localStorage.getItem("lang") || "hu";
  setLanguage(saved);

  const btn = document.getElementById("lang-toggle");
  if (btn) {
    btn.addEventListener("click", () => {
      const current = localStorage.getItem("lang") || "hu";
      const newLang = current === "hu" ? "en" : "hu";
      setLanguage(newLang);
    });
  }
}

